const loadLanguage = () => {
    const savedLang = localStorage.getItem("selectedLang") || "ar";
    updatePageLanguage(savedLang);
    document.getElementById("language-toggle").checked = savedLang === "en";
};

const updatePageLanguage = (lang) => {
    const selectedLang = translations[lang];

    const navItems = document.querySelectorAll(".nav-menu-wrapper .navbar-nav .nav-item a");
    const keys = ["home", "about", "services", "products", "Contact"];
    
    navItems.forEach((item, index) => {
        item.textContent = selectedLang.nav[keys[index]];
    });
    document.querySelector(".section-title h1").innerHTML = selectedLang.heroTitle;
    document.querySelector(".section-title p").textContent = selectedLang.heroText;
    document.querySelector(".hero-btn a").textContent = selectedLang.buttonText;
    document.querySelector(".satisficed-client-box p").textContent = selectedLang.clientText;

    const selectedLangabout = translations[lang].aboutUs;
    document.querySelector(".about-us .section-title h3").textContent = selectedLangabout.title;
    document.querySelector(".about-us .section-title h2").innerHTML = selectedLangabout.heading;
    document.querySelector(".about-us .about-us-body p").textContent = selectedLangabout.text;
    document.querySelector(".about-us .about-experience-box p").innerHTML = selectedLangabout.experience;
    document.querySelector(".about-us .about-us-btn a").textContent = selectedLangabout.contactNow;
    document.querySelector(".about-us .about-author-content p").textContent = selectedLangabout.authorRole;


    document.querySelector(".about-list-content.one h3").textContent = selectedLang.aboutListcontentOne;
    document.querySelector(".about-list-content.two h3").textContent = selectedLang.aboutListcontentTwo;
    document.querySelector(".about-list-content.three h3").textContent = selectedLang.aboutListcontentThree;

    const selectedLangServices = translations[lang].services;
    document.querySelector(".our-services .section-title h3").textContent = selectedLangServices.title;
    document.querySelector(".our-services .section-title h2").innerHTML = selectedLangServices.heading;
    document.querySelector(".our-services .section-btn a").textContent = selectedLangServices.viewAll;
    document.querySelector(".our-services .service-footer p").innerHTML = selectedLangServices.consultation;
    
    const serviceItems = document.querySelectorAll(".our-services .service-item");
    serviceItems.forEach((item, index) => {
        item.querySelector(".service-item-content h3").textContent = selectedLangServices.items[index].title;
        item.querySelector(".service-item-content p").textContent = selectedLangServices.items[index].text;
        item.querySelector(".service-item-content a").textContent = selectedLangServices.items[index].more;
    });

    const selectedLangbrands = translations[lang].brands;
    
    document.querySelector(".our-brand-contant .section-title h3").textContent = selectedLangbrands.title;
    document.querySelector(".our-brand-contant .section-title h2").innerHTML = selectedLangbrands.heading;
    document.querySelector(".our-brand-btn a").textContent = selectedLangbrands.contact;
    
    const brandItems = document.querySelectorAll(".brand-list-item");
    brandItems.forEach((item, index) => {
        if (selectedLangbrands.items[index]) {
            item.querySelector(".brand-item-content h3").textContent = selectedLangbrands.items[index].title;
            item.querySelector(".brand-item-content p").textContent = selectedLangbrands.items[index].text;
        }
    });


    document.querySelector(".section-title.project h3").textContent = translations[lang].titleProject;
    document.querySelector(".section-title.project h2").innerHTML = translations[lang].subtitle;
    
    document.querySelectorAll(".blog-item-btn a").forEach(btn => {
        btn.textContent = translations[lang].viewMore;
    });
    document.querySelectorAll(".section-btn.blog  a").forEach(btn => {
        btn.textContent = translations[lang].viewMore;
    });

    document.querySelectorAll(".post-item-content h3 a").forEach((title, index) => {
        title.textContent = translations[lang].projects[index];
    });

    document.querySelectorAll(".pricing-item").forEach((item, index) => {
        item.querySelector(".pricing-header h3").textContent = translations[lang].packages[index].title;
        item.querySelector(".pricing-header p").textContent = translations[lang].packages[index].description;
        item.querySelector(".pricing-header h2").innerHTML = translations[lang].packages[index].price;
        item.querySelector(".pricing-body a").textContent = translations[lang].packages[index].button;
        
        const featuresList = item.querySelector(".pricing-footer ul");
        featuresList.innerHTML = "";
        translations[lang].packages[index].features.forEach(feature => {
            const li = document.createElement("li");
            li.textContent = feature;
            featuresList.appendChild(li);
        });
    });

    document.querySelector(".section-title.testimonial h3").textContent = translations[lang].testimonialsTitle;
    document.querySelector(".section-title.testimonial h2").innerHTML = translations[lang].testimonialsSubtitle;
    document.querySelector(".section-btn.testimonial a").textContent = translations[lang].contactUs;
    
    document.querySelectorAll(".testimonial-item").forEach((item, index) => {
        if (translations[lang].testimonials[index]) {
            item.querySelector(".testimonial-content p").textContent = translations[lang].testimonials[index].text;
            item.querySelector(".testimonial-author h3").textContent = translations[lang].testimonials[index].name;
            item.querySelector(".testimonial-author p").textContent = translations[lang].testimonials[index].role;
        }
    });
    
    document.querySelectorAll(".testimonial-counter p").forEach(counter => {
        counter.textContent = translations[lang].satisfactionRate;
    });



    document.querySelector("title").textContent = selectedLang.title;
    document.documentElement.setAttribute("lang", lang);
    document.documentElement.setAttribute("dir", lang === "en" ? "ltr" : "rtl");

    document.querySelector(".switch .on").textContent = lang === "ar" ? "ar" : "ar";
    document.querySelector(".switch .off").textContent = lang === "ar" ? "en" : "en";
};

